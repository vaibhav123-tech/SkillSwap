const mongoose = require('mongoose');

const authUserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    password: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const AuthUser = mongoose.model('AuthUser', authUserSchema);

module.exports = AuthUser;